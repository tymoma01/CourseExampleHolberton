pwd 

# show where you are (“print working directory”)

ls 

# list what’s here (try ls -l for details, ls -la to include hidden files)

cd <place> 

# go somewhere (e.g., cd /root, cd .. to go up)

cat <file> 

# show a file’s content

less <file> 

# scroll through a file (q to quit)

touch <file> 

# create an empty file

cp <src> <dst> 

# copy files or folders (-r for folders)

mv <src> <dst> 

# move or rename

rm <file> 

# remove a file

mkdir <name> 

# make a folder

rmdir <name> 

# remove an empty folder

man <command> 

# the manual (press q to quit)