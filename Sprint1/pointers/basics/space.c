#include <stdio.h>

int main(void)
{
    int n;

    printf("sizeof(char)  = %lu\n", (unsigned long)sizeof(char));
    printf("sizeof(int)   = %lu\n", (unsigned long)sizeof(int));
    printf("sizeof(float) = %lu\n", (unsigned long)sizeof(float));
    printf("sizeof(n)     = %lu\n", (unsigned long)sizeof(n));

    return 0;
}
